def gen_compute_body(state, model, d_inner_spec, has_adam=True,
                     is_meta_only=False, indent="    "):
    """Generate the full per-element compute body (6-step pipeline)."""
    I = indent
    lines = []

    # MoE early exit
    if model == "moe":
        lines.append(f"{I}// MoE early-exit: skip inactive expert parameters")
        lines.append(f"{I}if (!active_mask[idx]) return;")
        lines.append("")

    lines.append(f"{I}float g = static_cast<float>(grad[idx]);")
    lines.append(f"{I}float s = static_cast<float>(sharpness[idx]);")
    lines.append(f"{I}if (!isfinite(g)) g = 0.0f;")
    lines.append(f"{I}if (!isfinite(s)) s = 0.0f;")
    lines.append(f"{I}const int half_d = d_model / 2;")
    lines.append(f"{I}const int peer_input_dim = gru_hidden + 2 * d_model + 2;")
    lines.append("")

    # d_inner may be compile-time or runtime
    din = str(d_inner_spec) if d_inner_spec > 0 else "d_inner"
    unroll = "#pragma unroll" if d_inner_spec > 0 else ""

    # ═══ Step 1: Out_proj — project scan outputs ═══
    lines.append(f"{I}// Step 1: out_proj — project scan outputs to fwd_ctx/bwd_ctx")
    lines.append(f"{I}float fwd_ctx[MAX_D_MODEL], bwd_ctx[MAX_D_MODEL];")
    lines.append(f"{I}for (int dm = 0; dm < d_model; dm++) {{")
    lines.append(f"{I}    float fwd_acc = 0.0f, bwd_acc = 0.0f;")
    if unroll:
        lines.append(f"{I}    {unroll}")
    lines.append(f"{I}    for (int di = 0; di < {din}; di++) {{")
    lines.append(f"{I}        float fwd_so = fwd_scan_out[idx * {din} + di];")
    lines.append(f"{I}        float bwd_so = bwd_scan_out[idx * {din} + di];")
    lines.append(f"{I}        fwd_acc += s_out_fwd[dm * {din} + di] * fwd_so;")
    lines.append(f"{I}        bwd_acc += s_out_bwd[dm * {din} + di] * bwd_so;")
    lines.append(f"{I}    }}")
    lines.append(f"{I}    fwd_ctx[dm] = fwd_acc;")
    lines.append(f"{I}    bwd_ctx[dm] = bwd_acc;")
    lines.append(f"{I}}}")
    lines.append("")

    # ═══ Step 2: GRU update ═══
    lines.append(f"{I}// Step 2: GRU state update")
    # Load old GRU state with non-temporal access for FP32, or dequantize for Q4
    if state == "fp32":
        lines.append(f"{I}float h_old[MAX_GRU_HIDDEN];")
        lines.append(f"{I}for (int gh = 0; gh < gru_hidden; gh++)")
        lines.append(f"{I}    h_old[gh] = stream_load(&gru_state[idx * gru_hidden + gh]);")
    else:  # q4: BF16+SR
        lines.append(f"{I}float h_old[MAX_GRU_HIDDEN];")
        lines.append(f"{I}for (int gh = 0; gh < gru_hidden; gh++)")
        lines.append(f"{I}    h_old[gh] = __bfloat162float(gru_state[idx * gru_hidden + gh]);")
    lines.append("")

    lines.append(f"{I}// Build GRU input vector: [g, s, fwd_ctx, bwd_ctx, h_old]")
    lines.append(f"{I}float gru_in[2 + 2 * MAX_D_MODEL + MAX_GRU_HIDDEN];")
    lines.append(f"{I}gru_in[0] = g; gru_in[1] = s;")
    lines.append(f"{I}for (int dm = 0; dm < d_model; dm++) gru_in[2 + dm] = fwd_ctx[dm];")
    lines.append(f"{I}for (int dm = 0; dm < d_model; dm++) gru_in[2 + d_model + dm] = bwd_ctx[dm];")
    lines.append(f"{I}for (int gh = 0; gh < gru_hidden; gh++) gru_in[2 + 2 * d_model + gh] = h_old[gh];")
    lines.append(f"{I}const int gru_in_len = 2 + 2 * d_model + gru_hidden;")
    lines.append("")

    # GRU gates
    lines.append(f"{I}float h_new[MAX_GRU_HIDDEN];")
    lines.append(f"{I}for (int gh = 0; gh < gru_hidden; gh++) {{")
    lines.append(f"{I}    float z_raw = s_gru_bz[gh], r_raw = s_gru_br[gh];")
    lines.append(f"{I}    for (int k = 0; k < gru_in_len; k++) {{")
    lines.append(f"{I}        z_raw += s_gru_Wz[gh * gru_row_len + k] * gru_in[k];")
    lines.append(f"{I}        r_raw += s_gru_Wr[gh * gru_row_len + k] * gru_in[k];")
    lines.append(f"{I}    }}")
    lines.append(f"{I}    float z_gate = ptx_sigmoidf(z_raw);")
    lines.append(f"{I}    float r_gate = ptx_sigmoidf(r_raw);")
    lines.append(f"{I}    // Candidate with reset applied to h_old portion")
    lines.append(f"{I}    float h_raw = s_gru_bh[gh];")
    lines.append(f"{I}    for (int k = 0; k < gru_in_len; k++) {{")
    lines.append(f"{I}        float inp_k = gru_in[k];")
    lines.append(f"{I}        if (k >= 2 + 2 * d_model) inp_k = r_gate * inp_k;  // reset h_old")
    lines.append(f"{I}        h_raw += s_gru_Wh[gh * gru_row_len + k] * inp_k;")
    lines.append(f"{I}    }}")
    lines.append(f"{I}    float h_tilde = ptx_tanhf(h_raw);")
    lines.append(f"{I}    h_new[gh] = (1.0f - z_gate) * h_old[gh] + z_gate * h_tilde;")
    lines.append(f"{I}}}")
    lines.append("")

    # Store new GRU state
    if state == "fp32":
        lines.append(f"{I}for (int gh = 0; gh < gru_hidden; gh++)")
        lines.append(f"{I}    stream_store(&gru_state[idx * gru_hidden + gh], h_new[gh]);")
    else:  # q4: BF16+SR
        lines.append(f"{I}for (int gh = 0; gh < gru_hidden; gh++) {{")
        lines.append(f"{I}    unsigned _rng = hash_prng(global_step * 7u + (unsigned)gh, (unsigned)idx);")
        lines.append(f"{I}    gru_state[idx * gru_hidden + gh] = float_to_bf16_stochastic(h_new[gh], _rng);")
        lines.append(f"{I}}}")
    lines.append("")

    # ═══ Step 3: Multi-head PEER routing + Expert MLP ═══
    lines.append(f"{I}// Step 3: Multi-head PEER routing + Expert MLP")
    lines.append(f"{I}// Build query input: [h_new, fwd_ctx, bwd_ctx, g, s]")
    lines.append(f"{I}float q_in[MAX_GRU_HIDDEN + 2 * MAX_D_MODEL + 2];")
    lines.append(f"{I}for (int gh = 0; gh < gru_hidden; gh++) q_in[gh] = h_new[gh];")
    lines.append(f"{I}for (int dm = 0; dm < d_model; dm++) q_in[gru_hidden + dm] = fwd_ctx[dm];")
    lines.append(f"{I}for (int dm = 0; dm < d_model; dm++) q_in[gru_hidden + d_model + dm] = bwd_ctx[dm];")
    lines.append(f"{I}q_in[gru_hidden + 2 * d_model] = g;")
    lines.append(f"{I}q_in[gru_hidden + 2 * d_model + 1] = s;")
    lines.append("")

    lines.append(f"{I}float total_out = 0.0f;")
    lines.append(f"{I}for (int head = 0; head < num_heads; head++) {{")
    lines.append(f"{I}    // Query projection")
    lines.append(f"{I}    float query[MAX_D_MODEL];")
    lines.append(f"{I}    for (int dm = 0; dm < d_model; dm++) {{")
    lines.append(f"{I}        float acc = 0.0f;")
    lines.append(f"{I}        for (int k = 0; k < peer_input_dim; k++)")
    lines.append(f"{I}            acc += peer_query_Ws[head * d_model * peer_input_dim + dm * peer_input_dim + k] * q_in[k];")
    lines.append(f"{I}        query[dm] = acc;")
    lines.append(f"{I}    }}")
    lines.append("")

    lines.append(f"{I}    // Product-key routing: argmax over each half")
    lines.append(f"{I}    float best_A = -1e30f, best_B = -1e30f;")
    lines.append(f"{I}    int idx_A = 0, idx_B = 0;")
    lines.append(f"{I}    for (int pk = 0; pk < pk_dim; pk++) {{")
    lines.append(f"{I}        float score_A = 0.0f, score_B = 0.0f;")
    lines.append(f"{I}        for (int dm = 0; dm < half_d; dm++) {{")
    lines.append(f"{I}            score_A += prod_keys_A[head * pk_dim * half_d + pk * half_d + dm] * query[dm];")
    lines.append(f"{I}            score_B += prod_keys_B[head * pk_dim * half_d + pk * half_d + dm] * query[half_d + dm];")
    lines.append(f"{I}        }}")
    lines.append(f"{I}        if (score_A > best_A) {{ best_A = score_A; idx_A = pk; }}")
    lines.append(f"{I}        if (score_B > best_B) {{ best_B = score_B; idx_B = pk; }}")
    lines.append(f"{I}    }}")
    lines.append(f"{I}    int expert_id = idx_A * pk_dim + idx_B;")
    lines.append(f"{I}    if (expert_id >= num_experts) expert_id = num_experts - 1;")
    lines.append(f"{I}    atomicAdd(&expert_counts[expert_id], 1);")
    lines.append("")

    lines.append(f"{I}    // Expert MLP: ReLU(W1 * g + b1) → W2 @ hidden + b2")
    lines.append(f"{I}    float head_out = s_expert_b2[expert_id];")
    lines.append(f"{I}    for (int eh = 0; eh < expert_hidden; eh++) {{")
    lines.append(f"{I}        float hidden = s_expert_W1[expert_id * expert_hidden + eh] * g + s_expert_b1[expert_id * expert_hidden + eh];")
    lines.append(f"{I}        hidden = fmaxf(hidden, 0.0f);  // ReLU")
    lines.append(f"{I}        head_out += s_expert_W2[expert_id * expert_hidden + eh] * hidden;")
    lines.append(f"{I}    }}")
    lines.append(f"{I}    total_out += head_out;")
    lines.append(f"{I}}}")
    lines.append("")

    # ═══ Step 4: Smart gradient ═══
    lines.append(f"{I}// Step 4: smart gradient")
    lines.append(f"{I}float smart_grad = g + rescale * (total_out / (float)num_heads);")
    lines.append("")

    if is_meta_only:
        lines.append(f"{I}// Meta-only: write smart_grad to output buffer, no Adam")
        lines.append(f"{I}smart_grad_out[idx] = smart_grad;")
        return "\n".join(lines)

    # ═══ Step 5: Mu EMA update ═══
    lines.append(f"{I}// Step 5: mu EMA update")
    if state == "fp32":
        lines.append(f"{I}float mu_old = stream_load(&mu[idx]);")
        lines.append(f"{I}float mu_new = alpha * mu_old + (1.0f - alpha) * g;")
        lines.append(f"{I}stream_store(&mu[idx], mu_new);")
    else:  # q4: BF16+SR for mu
        lines.append(f"{I}// Q4 BF16 mu load/store with stochastic rounding")
        lines.append(f"{I}float mu_old = __bfloat162float(mu[idx]);")
        lines.append(f"{I}float mu_new = alpha * mu_old + (1.0f - alpha) * g;")
        lines.append(f"{I}unsigned _mu_rng = hash_prng(global_step * 5u, (unsigned)idx);")
        lines.append(f"{I}mu[idx] = float_to_bf16_stochastic(mu_new, _mu_rng);")
    lines.append("")

    # ═══ Step 6: Adam optimizer step ═══
    lines.append(f"{I}// Step 6: Adam optimizer step")
    lines.append(f"{I}float effective_grad = smart_grad + lamb_eff * mu_new;")
    if state == "fp32":
        lines.append(f"{I}float ea_old = stream_load(&exp_avg[idx]);")
        lines.append(f"{I}float easq_old = stream_load(&exp_avg_sq[idx]);")
        lines.append(f"{I}float ea = beta1 * ea_old + (1.0f - beta1) * effective_grad;")
        lines.append(f"{I}float easq = beta2 * easq_old + (1.0f - beta2) * effective_grad * effective_grad;")
        lines.append(f"{I}stream_store(&exp_avg[idx], ea);")
        lines.append(f"{I}stream_store(&exp_avg_sq[idx], easq);")
    else:  # q4: INT8-block8
        lines.append(f"{I}// Q4 INT8 exp_avg load")
        lines.append(f"{I}int _ea_blk = idx / 8;")
        lines.append(f"{I}float ea_old = (float)exp_avg_q[idx] * exp_avg_scales[_ea_blk];")
        lines.append(f"{I}float ea = beta1 * ea_old + (1.0f - beta1) * effective_grad;")
        lines.append(f"{I}// Q4 INT8 exp_avg_sq load")
        lines.append(f"{I}float easq_old = (float)exp_avg_sq_q[idx] * exp_avg_sq_scales[_ea_blk];")
        lines.append(f"{I}float easq = beta2 * easq_old + (1.0f - beta2) * effective_grad * effective_grad;")
        lines.append(f"{I}// Stochastic requantize")
        lines.append(f"{I}unsigned _ea_rng = sg2_philox_hash(global_step * 3u, (unsigned)idx);")
        lines.append(f"{I}float _ea_scale = fmaxf(fabsf(ea), 1e-12f) / 127.0f;")
        lines.append(f"{I}exp_avg_q[idx] = float_to_int8_stochastic(ea, _ea_scale, _ea_rng);")
        lines.append(f"{I}if (idx % 8 == 0) exp_avg_scales[_ea_blk] = _ea_scale;")
        lines.append(f"{I}unsigned _easq_rng = sg2_philox_hash(global_step * 4u, (unsigned)idx);")
        lines.append(f"{I}float _easq_scale = fmaxf(fabsf(easq), 1e-12f) / 127.0f;")
        lines.append(f"{I}exp_avg_sq_q[idx] = float_to_int8_stochastic(easq, _easq_scale, _easq_rng);")
        lines.append(f"{I}if (idx % 8 == 0) exp_avg_sq_scales[_ea_blk] = _easq_scale;")
    lines.append("")

    lines.append(f"{I}// Param update with weight decay")
    lines.append(f"{I}float step_size = lr / bc1;")
    lines.append(f"{I}float denom = sqrtf(easq / bc2) + eps;")
    lines.append(f"{I}float p = static_cast<float>(param[idx]);")
    lines.append(f"{I}p *= (1.0f - lr * wd_eff);")
    lines.append(f"{I}p -= step_size * ea / denom;")
    lines.append(f"{I}param[idx] = static_cast<scalar_t>(p);")

    return "\n".join(lines)


