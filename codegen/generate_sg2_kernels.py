def gen_launcher(kname, state, expert, model, is_meta=False, is_pscan=False):
    """Generate a C++ launcher function for the given kernel."""
    lname = launcher_name(kname)
    lines = []
    lines.append(f"void {lname}(")

    # Torch tensor params
    tensor_params = []
    if not is_meta:
        tensor_params.append("    torch::Tensor param")
    tensor_params.append("    torch::Tensor grad")
    tensor_params.append("    torch::Tensor sharpness")

    if is_meta:
        tensor_params.append("    torch::Tensor gru_state")
        tensor_params.append("    torch::Tensor smart_grad_out")
    elif state == "fp32":
        tensor_params.append("    torch::Tensor exp_avg")
        tensor_params.append("    torch::Tensor exp_avg_sq")
        tensor_params.append("    torch::Tensor mu")
        tensor_params.append("    torch::Tensor gru_state")
    else:
        tensor_params.append("    torch::Tensor exp_avg_q")
        tensor_params.append("    torch::Tensor exp_avg_scales")
        tensor_params.append("    torch::Tensor exp_avg_sq_q")
        tensor_params.append("    torch::Tensor exp_avg_sq_scales")
        tensor_params.append("    torch::Tensor mu")
        tensor_params.append("    torch::Tensor gru_state")

    if is_pscan:
        tensor_params.append("    torch::Tensor scan_A")
        tensor_params.append("    torch::Tensor scan_B")
        tensor_params.append("    torch::Tensor scan_state")
    else:
        tensor_params.append("    torch::Tensor fwd_scan_out")
        tensor_params.append("    torch::Tensor bwd_scan_out")

    # Scalar params
    scalar_params = []
    if not is_meta:
        scalar_params.extend([
            "    float rescale, float alpha, float lamb_eff",
            "    float beta1, float beta2, float lr",
            "    float wd_eff, float eps, float bc1, float bc2",
        ])
    else:
        scalar_params.append("    float rescale")

    if state == "q4" and not is_meta:
        scalar_params.append("    int global_step")

    scalar_params.extend([
        "    int d_model, int d_inner",
        "    int gru_hidden, int num_heads, int pk_dim",
        "    int expert_hidden, int num_experts",
    ])

    all_params = tensor_params + scalar_params
    lines.append(",\n".join(all_params))
    lines.append(") {")
    lines.append(f"    const int N = grad.numel();")
    lines.append(f"    if (N == 0) return;")
    lines.append(f"    const int grid = (N + 256 - 1) / 256;")
    lines.append(f"    // TODO: compute shared memory size and launch {kname}")
    lines.append(f"    // Shared memory = 2*op_size + 3*gru_mat_size + 3*gru_hidden")
    lines.append(f"    //               + 3*num_experts*expert_hidden + num_experts (all float)")
    lines.append("}")
    lines.append("")

    return "\n".join(lines)


