def _d_inner_suffix(d_inner_spec):
    """Return naming suffix for d_inner specialization (0=none, 16='d16', 8='d8')."""
    if d_inner_spec == 16:
        return "d16"
    elif d_inner_spec == 8:
        return "d8"
    return None


def fused_elem_kernel_name(state, expert, hw, model, d_inner_spec):
    """
    Build kernel name:
      sg2_fused_elem_{state}_{expert}_{hw}[_moe][_d16|_d8]_kernel
    """
    parts = ["sg2_fused_elem", state, expert, hw]
    if model == "moe":
        parts.append("moe")
    ds = _d_inner_suffix(d_inner_spec)
    if ds:
        parts.append(ds)
    parts.append("kernel")
    return "_".join(parts)


def meta_only_kernel_name(expert, hw, d_inner_spec):
    parts = ["sg2_meta_only", expert, hw]
    ds = _d_inner_suffix(d_inner_spec)
    if ds:
        parts.append(ds)
    parts.append("kernel")
    return "_".join(parts)


def persistent_scan_kernel_name(state, hw, d_inner_spec):
    parts = ["persistent_scan_fused_elem", state, hw]
    ds = _d_inner_suffix(d_inner_spec)
    if ds:
        parts.append(ds)
    parts.append("kernel")
    return "_".join(parts)


def launcher_name(kernel_name):
    return "launch_" + kernel_name.replace("_kernel", "")


