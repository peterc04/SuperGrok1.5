def is_existing_kernel(state, expert, hw, model, d_inner_spec):
    """Check if this variant already exists as a hand-written kernel."""
    # Existing kernels: fp32 state, dense model, generic d_inner
    if state == "fp32" and model == "dense" and d_inner_spec == 0:
        if expert == "fp32" and hw == "generic":
            return True  # fused_elem_step_kernel
        if expert == "int8" and hw == "generic":
            return True  # fused_elem_step_int8_kernel
        if expert == "int4" and hw == "generic":
            return True  # fused_elem_step_int4_kernel
        if expert == "mxfp4" and hw == "generic":
            return True  # fused_elem_step_mxfp4_kernel
        if expert == "fp32" and hw == "cpasync":
            return True  # fused_elem_step_cpasync_kernel
    return False


def main():
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    # ── Collect all kernels ──────────────────────────────────────────
    fused_elem_kernels = []
    meta_only_kernels = []
    persistent_scan_kernels = []

    # Fused elem: State(2) x Expert(4) x HW(2) x Model(2) x d_inner(3)
    # = 96 total, minus 5 existing = 91 new
    for state, expert, hw, model, d_inner_spec in itertools.product(
        STATES, EXPERTS, HARDWARE, MODELS, D_INNER_OPTS
    ):
        if is_existing_kernel(state, expert, hw, model, d_inner_spec):
            continue
        fused_elem_kernels.append((state, expert, hw, model, d_inner_spec))

    # Meta-only: Expert(4) x HW(2) x d_inner(2: generic + d16 only) = 16
    for expert, hw, d_inner_spec in itertools.product(EXPERTS, HARDWARE, [0, 16]):
        meta_only_kernels.append((expert, hw, d_inner_spec))

    # Persistent scan: State(2) x HW(2) x d_inner(2: generic + d16) = 8
    # minus 1 existing (fp32_generic_0) = 7
    for state, hw, d_inner_spec in itertools.product(STATES, HARDWARE, [0, 16]):
        persistent_scan_kernels.append((state, hw, d_inner_spec))

    # Remove existing persistent scan variant
    persistent_scan_kernels = [
        (s, h, d) for s, h, d in persistent_scan_kernels
        if not (s == "fp32" and h == "generic" and d == 0)
    ]

    total_fused = len(fused_elem_kernels)
    total_meta = len(meta_only_kernels)
    total_pscan = len(persistent_scan_kernels)

    print(f"SG2 fused_elem kernel variants to generate: {total_fused}")
    print(f"  (5 existing hand-written kernels skipped, 96 total)")
    print(f"Meta-only kernel variants: {total_meta}")
    print(f"Persistent scan fusion variants: {total_pscan}")
    print(f"SG2 Adam-only variants: 8 (already generated by Phase 1, skipped)")
    print(f"Total new kernels: {total_fused + total_meta + total_pscan}")

    # ── Generate: split across multiple files to manage compile times ──

    # File 1: fused_elem kernels (FP32 state)
    fp32_kernels = [(s, e, h, m, d) for s, e, h, m, d in fused_elem_kernels if s == "fp32"]
    _write_fused_file("sg2_fused_elem_fp32_generated.cu", fp32_kernels, "FP32 state")

    # File 2: fused_elem kernels (Q4 state)
    q4_kernels = [(s, e, h, m, d) for s, e, h, m, d in fused_elem_kernels if s == "q4"]
    _write_fused_file("sg2_fused_elem_q4_generated.cu", q4_kernels, "Q4 state")

    # File 3: meta-only kernels
    _write_meta_file("sg2_meta_only_generated.cu", meta_only_kernels)

    # File 4: persistent scan fusion kernels
    _write_pscan_file("sg2_persistent_scan_generated.cu", persistent_scan_kernels)

    print(f"\nGenerated files in {OUTPUT_DIR}/:")
    print(f"  sg2_fused_elem_fp32_generated.cu  ({len(fp32_kernels)} kernels)")
    print(f"  sg2_fused_elem_q4_generated.cu    ({len(q4_kernels)} kernels)")
    print(f"  sg2_meta_only_generated.cu        ({total_meta} kernels)")
    print(f"  sg2_persistent_scan_generated.cu  ({total_pscan} kernels)")


def _write_fused_file(filename, kernels, desc):
    """Write a fused_elem kernel file."""
    path = OUTPUT_DIR / filename
    with open(path, "w") as f:
        f.write(FILE_HEADER)
        f.write(f"// {len(kernels)} SG2 fused_elem kernel variants ({desc})\n\n")

        for state, expert, hw, model, d_inner_spec in kernels:
            kname = fused_elem_kernel_name(state, expert, hw, model, d_inner_spec)
            tag = f"{state}/{expert}/{hw}"
            if model == "moe":
                tag += "/moe"
            ds = _d_inner_suffix(d_inner_spec)
            if ds:
                tag += f"/{ds}"
            f.write(f"// ═══════════════════════════════════════════════════════════\n")
            f.write(f"//  {kname}  [{tag}]\n")
            f.write(f"// ═══════════════════════════════════════════════════════════\n\n")
            f.write(gen_fused_elem_kernel(state, expert, hw, model, d_inner_spec))
            f.write("\n")
            f.write(gen_launcher(kname, state, expert, model))
            f.write("\n")


