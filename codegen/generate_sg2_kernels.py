def gen_persistent_scan_kernel(state, hw, d_inner_spec):
    """Generate a persistent scan fusion kernel stub."""
    kname = persistent_scan_kernel_name(state, hw, d_inner_spec)
    lines = []

    if d_inner_spec > 0:
        lines.append(f"template <typename scalar_t, int D_INNER={d_inner_spec}>")
    else:
        lines.append(f"template <typename scalar_t>")

    lines.append(f"__launch_bounds__(256, 2)")
    lines.append(f"__global__ void {kname}(")

    params = []
    params.append("    scalar_t* __restrict__ param")
    params.append("    const scalar_t* __restrict__ grad")
    params.append("    const scalar_t* __restrict__ sharpness")

    for p in state_params_decl(state):
        params.append(f"    {p}")

    # Scan inputs (persistent scan fuses the scan computation)
    params.append("    const float* __restrict__ scan_A")        # [N, d_inner] decay
    params.append("    const float* __restrict__ scan_B")        # [N, d_inner] input
    params.append("    float* __restrict__ scan_state")          # [d_inner] running state
    params.append("    const float* __restrict__ out_proj_fwd_W")
    params.append("    const float* __restrict__ out_proj_bwd_W")
    params.append("    const float* __restrict__ gru_Wz, const float* __restrict__ gru_bz")
    params.append("    const float* __restrict__ gru_Wr, const float* __restrict__ gru_br")
    params.append("    const float* __restrict__ gru_Wh, const float* __restrict__ gru_bh")
    params.append("    const float* __restrict__ peer_query_Ws")
    params.append("    const float* __restrict__ prod_keys_A")
    params.append("    const float* __restrict__ prod_keys_B")
    params.append("    const float* __restrict__ expert_W1")
    params.append("    const float* __restrict__ expert_b1")
    params.append("    const float* __restrict__ expert_W2")
    params.append("    const float* __restrict__ expert_b2")
    params.append("    const float rescale, const float alpha, const float lamb_eff")
    params.append("    const float beta1, const float beta2, const float lr")
    params.append("    const float wd_eff, const float eps, const float bc1, const float bc2")

    if state == "q4":
        params.append("    const unsigned global_step")

    params.append("    int* __restrict__ expert_counts")
    params.append("    const int N, const int d_model, const int d_inner")
    params.append("    const int gru_hidden, const int num_heads, const int pk_dim")
    params.append("    const int expert_hidden, const int num_experts")

    param_str = ",\n".join(params)
    lines.append(param_str)
    lines.append(") {")

    lines.append(gen_smem_layout())
    lines.append("")
    lines.append(gen_outproj_gru_load(hw))
    lines.append("")
    lines.append(gen_expert_load("fp32", hw))
    lines.append("")

    # Persistent scan: each thread processes one scan dimension,
    # sequentially over N timesteps, then fuses the elem update.
    din = str(d_inner_spec) if d_inner_spec > 0 else "d_inner"
    lines.append("    // Persistent scan: sequential over N, fused with elem step")
    lines.append(f"    const int tid_dim = threadIdx.x;")
    lines.append(f"    if (tid_dim >= {din}) return;")
    lines.append("")
    lines.append(f"    // Running scan state in registers")
    lines.append(f"    float scan_h = (scan_state != nullptr) ? scan_state[tid_dim] : 0.0f;")
    lines.append("")
    lines.append(f"    for (int step = 0; step < N; step++) {{")
    lines.append(f"        // Scan recurrence: h = A * h + B")
    lines.append(f"        float a_val = scan_A[step * {din} + tid_dim];")
    lines.append(f"        float b_val = scan_B[step * {din} + tid_dim];")
    lines.append(f"        scan_h = a_val * scan_h + b_val;")
    lines.append(f"    }}")
    lines.append("")
    lines.append(f"    // Write final scan state")
    lines.append(f"    if (scan_state != nullptr) scan_state[tid_dim] = scan_h;")
    lines.append("")
    lines.append(f"    // Now apply fused_elem for each element using the scan output")
    lines.append(f"    // Grid-stride over N with fused elem pipeline")
    lines.append(f"    for (int idx = blockIdx.x * blockDim.x + threadIdx.x; idx < N; idx += gridDim.x * blockDim.x) {{")
    lines.append(gen_compute_body(state, "dense", d_inner_spec))
    lines.append(f"    }}")
    lines.append("}")
    lines.append("")

    return "\n".join(lines)


